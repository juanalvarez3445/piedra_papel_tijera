# Cree un programa que calcule el promedio de tres notas para n estudiantes
for i in range (2):
    nombre=input("Escriba el nombre del estudiante :")
    n1=int(input("Ingrese la primera nota:"))
    n2=int(input("Ingrese la primera nota:"))
    n3=int(input("Ingrese la primera nota:"))
    promedio =(n1+n2+n3)/3
    print (f"El nombre del estudiante es {nombre} y su promedio es {promedio}")