// Crear un array con números
const numeros = [5, 3, 9, 1, 7];

// Encontrar el valor máximo y mínimo utilizando Math.max y Math.min
const maximo = Math.max(...numeros);
const minimo = Math.min(...numeros);

// Imprimir el valor máximo y mínimo
console.log("Valor máximo:", maximo);
console.log("Valor mínimo:", minimo);